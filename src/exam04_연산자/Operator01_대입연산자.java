package exam04_연산자;

public class Operator01_대입연산자 {

	public static void main(String[] args) {
		
		int n = 10;
		int n2 = 3;
		
		n+=n2; //n+n2=n
		System.out.println(n+"\t"+n2);
		n-=n2; //n-n2=n
		System.out.println(n+"\t"+n2);
		n*=n2;
		System.out.println(n+"\t"+n2);
		n/=n2;
		System.out.println(n+"\t"+n2);
		n%=n2;
		System.out.println(n+"\t"+n2);
		;



	}

}
